def numero_favorito(): # Funções e TDD. 
    return 42


def soma(a, b):
    return a + b


def ola_mundo():
    print("Olá, mundo!")

def subtracao(x, y):
    return x - y

def multiplicacao(x, y):
    return x * y

print(numero_favorito())  # Saída: 42
ola_mundo()  # Saída: Olá, mundo!
print(soma(1, 2))  # Saída: 3
print(subtracao(10, 6)) # Saída: 4
print(multiplicacao(4, 5)) # Saída: 20
